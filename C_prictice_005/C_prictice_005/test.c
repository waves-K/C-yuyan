//然后我们还需要把这个菱形拆分为 上半部分 和 下半部分进行考虑
//接下来我们就应该分别计算 上半部分 和 下半部分 的 空格数 以及 *
//
//假设我们要打印一个行数为 11 的菱形
//	设 n 为行号，每一行需要打印的 空格 数为 i, 每一行需要打印的 * 个数为 j
//
//上半部分：
//	那么上半部分行数就是 6 ，即 line = 6
//
//	n = 1时：
//		即 第一行 我们需要打印 5 个 空格，即需要打印的空格数为 i = 6 - 1 = line - n = 5 ;
//		   第一行 我们需要打印 1 个 * ，即需要打印 * 的个数为  j = 1 ;
//	n = 2时：
//		即 第二行 我们需要打印 4 个 空格，即需要打印的空格数为 i = 6 - 2 = line - n = 4 ;
//		   第二行 我们需要打印 3 个 * ，即需要打印 * 的个数为  j = 3  = 2 * 2 - 1; 
//	n = 3时：
//		即 第三行 我们需要打印 3 个 空格，即需要打印的空格数为 i = 6 - 3 = line - n = 3 ;
//		   第三行 我们需要打印 5 个 * ，即需要打印 * 的个数为  j = 5  = 2 * 3 - 1;
//	n = 4时：
