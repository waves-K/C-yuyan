//		   第二行 我们需要打印 7 个 * ，即需要打印 * 的个数为 j = 7 = 2 * (6 - 2) - 1 ;
//	n = 3时：
//		即 第三行 我们需要打印 3 个 空格，即需要打印的空格数为 i = 3 = n ;
//		   第三行 我们需要打印 5 个 * ，即需要打印 * 的个数为 j = 5 = 2 * (6 - 3) - 1 ;
//	n = 4时：
//		即 第四行 我们需要打印 4 个 空格，即需要打印的空格数为 i = 4 = n ;
